    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

       
        String startNode = jTextField1.getText();
        String endNode = jTextField2.getText();
        if (startNode == null || endNode == null){
            System.out.println("alert invalid node");
        }
        Dijkstra dijkstra = new Dijkstra(Dijkstra.Element.EDGE, "result", "weight");

        // Compute the shortest paths in g from A to all nodes
        dijkstra.init(graph);
        dijkstra.setSource(graph.getNode(startNode.trim()));
        graph.getNode(startNode.trim()).addAttribute("ui.style","fill-color: #54a863;" );
        dijkstra.compute();
       
        for (Node node : dijkstra.getPathNodes(graph.getNode(endNode.trim())))
             node.addAttribute("ui.style", "fill-color: #54a863;");

        // Color in red all the edges in the shortest path tree
        for (Edge edge : dijkstra.getPathEdges(graph.getNode(endNode.trim())))
            edge.addAttribute("ui.style", "fill-color: #71e886; size: 3px;");

         dijkstra.clear();
      
    }//GEN-LAST:event_jButton3ActionPerformed

