    public static void main(String args[]) {
        /* Set the Nimbus look and feel */
      
       window myWindow = new window();
       //JPanel panel = new JPanel();
        try
        {
            //File fr =new File("./input.txt");
            Scanner input = new Scanner(new BufferedReader(new FileReader(args[0])));
                      // String css = "edge {size:1px;fill-color:gray;} " ;
                       
            graph.setStrict(false);
            int numNode = 0;
            String edge = "";
            while(input.hasNext())
            {
                String s = input.next();
                String node = s.substring(0,1);
                if (numNode == 2){
                    graph.addEdge(edge,edge.substring(0,1), edge.substring(1,2),true).addAttribute("weight",node);
                    graph.getEdge(edge).addAttribute("ui.label",node);
                    numNode=0;
                    edge = "";
                }
                else if(numNode < 2){
                    edge += node;               
                    graph.addNode(node);  
                    graph.getNode(node).addAttribute("ui.label",node);                                       
                    numNode ++;
                }
                
            }
           graph.addAttribute("ui.stylesheet",css); 
     
            Viewer viewer =new Viewer(graph, Viewer.ThreadingModel.GRAPH_IN_GUI_THREAD);
            viewer.enableAutoLayout();
            ViewPanel view =  viewer.addDefaultView(false);

            view.setSize(600,550);

            myWindow.jPanel1.add(view);

            myWindow.pack();
        }
        catch(ArrayIndexOutOfBoundsException ex)
        {
            System.out.println("\nPlease Enter the File Name in Command Line Argument. \n"+
            "For Example :- java MyFileReader C:\\Users\\Desktop\\File1.txt");
        }
        catch(IOException ex)
        {
                System.out.println("File Does Not Found in given Directory. ");
        }
               

        myWindow.setVisible(true);
	
    }

